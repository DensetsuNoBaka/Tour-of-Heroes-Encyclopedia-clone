using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using Newtonsoft.Json;
using Tour_of_Heroes.Classes;

namespace Tour_of_Heroes.Controllers
{
    [ApiController]
    [Route("[controller]")]
    public class WeatherForecastController : ControllerBase
    {
        private static readonly string[] Summaries = new[]
        {
            "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
        };

        private readonly ILogger<WeatherForecastController> _logger;

        public WeatherForecastController(ILogger<WeatherForecastController> logger)
        {
            _logger = logger;
        }

        [HttpGet]
        public IEnumerable<WeatherForecast> Get()
        {
            var rng = new Random();
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateTime.Now.AddDays(index),
                TemperatureC = rng.Next(-20, 55),
                Summary = Summaries[rng.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [Route("GetAssignment")]
        public JsonResult GetAssignment(int heroId)
        {
            Assignment assignment = new Assignment();
            string json = JsonConvert.SerializeObject(assignment.ConvertNumber(12345));
            return new JsonResult(json);
        }

        [Route("WrathTest/{tests}")]
        public string WrathTest(int tests)
        {
            Assignment assignment = new Assignment();
            return assignment.WrathTest(tests);
        }
    }
}
